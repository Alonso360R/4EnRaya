    private void CuadroMouseClicked(java.awt.event.MouseEvent evt) throws InterruptedException {
        // Se busca de donde provino el evento en el tablero
        JToggleButton temp = (JToggleButton) evt.getSource();
        if (temp.getIcon() == null) {
            juega((JToggleButton) evt.getSource());
        }else{
            evt.consume();
        }
    }

    private void Eventos_Juego() // agregamos los eventos a cada una de las posiciones en el tablero
    {
        for (int i = 0; i <= 35; i++) {
            tabla.get(i).addMouseListener(new java.awt.event.MouseAdapter() {
                public void mouseClicked(java.awt.event.MouseEvent evt) {
                    try {
                        CuadroMouseClicked(evt);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(Plataforma.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            });
        }
    }

    private void juega(JToggleButton boton) throws InterruptedException {   // llamamos al metodo jugar si el lugar seleccionado esta disponible
        float[] disponible2 = {153, 153, 255};
        disponible2 = boton.getBackground().getColorComponents(disponible2);
        if (disponible2[0] == (float) 0.6 && disponible2[1] == (float) 0.6 && disponible2[2] == (float) 1.0) {
            int i = 0;
            while (i <= 35) {
                if (tabla.get(i) != boton) {
                    i++;
                } else {
                    Jugar(jlTurno.getText(), i);
                    i = 36;
                }
            }
        }
    }

    //llamar a jugada computador
    private void Jugar(String jugador, int pos) throws InterruptedException {
        if (jlJugador.getText().equals(jugador)) {

            tabla.get(pos).setIcon(icono_player1); // Actualizamos el tablero con la jugada del humano
            int temp = pos % 6; // Se obtiene la columna donde jugó el humano

            //Actualizamos el tablero en lisp con la jugada del humano
            juegaHumanoFun.execute(Fixnum.getInstance(temp));

            /* Se llama a la funcion de lisp
                que nos devuelve el estado actual del juego
                1 = Ganó el humano
               -1 = Ganó la maquina
                0 = Sigue el juego
             */
            int estado = estadoFun.execute().intValue();

            //Se comprueba si ganó el humano
            if (estado == 1) {
                JOptionPane.showMessageDialog(this, "HAS GANADO!");
                disableAll(); //esto desactiva todos los botones, hay que modificar

                // Se sigue jugando en caso contrario
            } else {
                if (pos + 6 <= 35) {
                    tabla.get(pos + 6).setBackground(disponible);
                }
                jlTurno.setText(jlPC.getText());
                JugadaComputador();
            }

        } else {
            tabla.get(pos).setIcon(icono_player2); // Actualizamos el tablero con la jugada de la maquina

            /* Se llama a la funcion de lisp
                que nos devuelve el estado actual del juego
                1 = Ganó el humano
               -1 = Ganó la maquina
                0 = Sigue el juego
             */
            int estado = estadoFun.execute().intValue();

            // Se comprueba si ganó la máquina
            if (estado == -1) {
                JOptionPane.showMessageDialog(this, "HAS PERDIDO!");
                disableAll(); //esto desactiva todos los botones, hay que modificar
            }
            if (pos + 6 <= 35) {
                tabla.get(pos + 6).setBackground(disponible);
            }
            jlTurno.setText(jlJugador.getText());
        }
    }

    private void JugadaComputador() throws InterruptedException {
        /* Obtendra la jugada de la maquina
            a partir del codigo lisp
            dependiendo de la dificultad que se eligio
         */
        int pos = juegaMaquinaFun.execute().intValue(); //Determinar jugada de computador
        //System.out.println(pos);
        Jugar("ordenador", pos);
    }

