    private void carga_lisp() {

        //Creamos el objeto que hara la conexion entre Java y Lisp
        interpreter = Interpreter.createInstance();
        interpreter.eval("(load \"proyecto_lisp_java\")");

        //Usaremos el package predeterminado en lisp
        Package defaultPackage = Packages.findPackage("CL-USER");

        // Se busca en el package definido las funciones indicadas en el parametro
        juegaHumanoSym = defaultPackage.findAccessibleSymbol("JUEGA-HUMANO");
        juegaMaquinaSym = defaultPackage.findAccessibleSymbol("JUEGA-MAQUINA");
        iniciaSym = defaultPackage.findAccessibleSymbol("INICIA-JUEGO");
        estadoSym = defaultPackage.findAccessibleSymbol("GETESTADO");

        // Obtenemos la "funcion" a partir de su nombre (del paso anterior)
        juegaHumanoFun = (Function) juegaHumanoSym.getSymbolFunction();
        juegaMaquinaFun = (Function) juegaMaquinaSym.getSymbolFunction();
        iniciaFun = (Function) iniciaSym.getSymbolFunction();
        estadoFun = (Function) estadoSym.getSymbolFunction();

        // Se ejecuta la funcion que inicia el juego en lisp
        // Con su respectiva dificultad
        switch (dificultad) {
            case "FACIL":
                iniciaFun.execute(Fixnum.getInstance(1), Fixnum.getInstance(1));
                break;
            case "INTERMEDIO":
                iniciaFun.execute(Fixnum.getInstance(1), Fixnum.getInstance(2));
                break;
            case "DIFICIL":
                iniciaFun.execute(Fixnum.getInstance(1), Fixnum.getInstance(3));
        }
    }

